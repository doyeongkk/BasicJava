package e_oop;

public class Calculator {

	
	//5 2개의 파라미터
	
	//
	double plus (double a, double b){
		return a + b;
	}
	
	double minus (double a, double b){
		return a - b;
	}
	
	double multi   (double a,  double b){
		return a * b;
	}
	
	double devision (double a, double b){
		return a / b;
	}
	
	double remain  (double a, double b){
		return a % b;
	}
}



